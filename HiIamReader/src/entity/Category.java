package entity;

/**
 *
 * @author Ngo Van Tuan
 */
public class Category {
    private String name;
    private String url;

    public Category() {
    }

    public Category(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
